const express = require('express');

const PORT = 5050;
const app = express();

//////////////////////////////////////////////////////////////////////////////////////
// View engine / config.
//////////////////////////////////////////////////////////////////////////////////////

// Enable EJS (make sure you npm install ejs first!)
app.set('view engine', 'ejs'); // HAML

//////////////////////////////////////////////////////////////////////////////////////
// Middleware
//////////////////////////////////////////////////////////////////////////////////////

// Logging Middleware.
app.use((req, res, next) => {
    console.log(`REQUEST: ${req.method} ${req.url}`);
    next(); // Next middleware!
});

//////////////////////////////////////////////////////////////////////////////////////
// Redirects
//////////////////////////////////////////////////////////////////////////////////////

// Redirect example.
app.get(['/', '/index', '/homes'], (req, res) => {
    res.status(301);
    res.redirect('/home');
});

//////////////////////////////////////////////////////////////////////////////////////
// Routes
//////////////////////////////////////////////////////////////////////////////////////

app.get('/home', (req, res) => {
    res.status(200);
    res.contentType('text/HTML');
    res.render('home');
});

app.get('/about', (req, res) => {
    res.status(200);
    res.contentType('text/HTML');

    const myName = 'Warren';
    const title  = 'Instructor';
    const myLangs = [
        'PHP',
        'HTML',
        'JavaScript',
        'CSS'
    ];
    const templateVariables = {
        name: myName, // PROPERTY NAME AVAIL. in TEMPLATE!
        position: title, // PROPERTY NAME AVAIL. in TEMPLATE!
        skills: myLangs
    };

    res.render('about', templateVariables);
});

//////////////////////////////////////////////////////////////////////////////////////
// 404 Handler
//////////////////////////////////////////////////////////////////////////////////////

// 404 Catch-all (not found) middleware.
app.use((req, res, next) => {
    res.status(404);
    res.contentType('text/html');
    res.render('404');
});

//////////////////////////////////////////////////////////////////////////////////////
// Server Listener
//////////////////////////////////////////////////////////////////////////////////////

app.listen(PORT, () => {
    console.log('Express app running on port:', PORT);
});
