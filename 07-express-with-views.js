const express = require('express');

/////////////////////////////////////////////////////////////////////////////////////
// Configuration
/////////////////////////////////////////////////////////////////////////////////////

const PORT = 7070;
const app = express();
app.set('view engine', 'ejs'); // Other options do exist, like HAML etc.

/////////////////////////////////////////////////////////////////////////////////////
// Middleware
/////////////////////////////////////////////////////////////////////////////////////

// Logging middleware.
app.use((req, res, next) => {
    console.log(
        'Request was received:',
        req.method, // GET / POST
        req.url // /home /about /blog/3
    );
    next(); // Continue to next middleware!
});

/////////////////////////////////////////////////////////////////////////////////////
// Routes
/////////////////////////////////////////////////////////////////////////////////////

app.get(['/', '/home'], (req, res) => {
    res.status(200);
    res.contentType('text/html');

    // We can pass object properties to the view.
    const templateVars = {
        pageName: 'Home'
    };

    res.render('home', templateVars);
});

app.get('/about', (req, res) => {
    res.status(200);
    res.contentType('text/html');
    res.render('about');
});

app.get('/contact', (req, res) => {
    res.status(200);
    res.contentType('text/html');
    res.render('contact');
});

/////////////////////////////////////////////////////////////////////////////////////
// 404: Page Not Found
/////////////////////////////////////////////////////////////////////////////////////

app.use((req, res) => {
    res.status(404);
    res.contentType('text/html');
    res.render('404');
});

/////////////////////////////////////////////////////////////////////////////////////
// Server Listener
/////////////////////////////////////////////////////////////////////////////////////

app.listen(PORT, () => {
    console.log('Express server is listening on port:', PORT);
});
