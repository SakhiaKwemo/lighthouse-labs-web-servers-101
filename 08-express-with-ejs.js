const express = require('express');

/////////////////////////////////////////////////////////////////////////////////
// Configuration
/////////////////////////////////////////////////////////////////////////////////

const PORT = 7070;
const app  = express();

app.set('view engine', 'ejs');

/////////////////////////////////////////////////////////////////////////////////
// Middleware
/////////////////////////////////////////////////////////////////////////////////

// Logging middleware:
app.use((req, res, next) => {
    console.log(req.method, req.url);
    next();
});

// Morgan.

/////////////////////////////////////////////////////////////////////////////////
// Listener
/////////////////////////////////////////////////////////////////////////////////

app.listen(PORT, () => {
    console.log(`Express application is now listening: http://localhost:${PORT}`);
});

/////////////////////////////////////////////////////////////////////////////////
// Routes
/////////////////////////////////////////////////////////////////////////////////

app.get(['/', '/home'], (req, res) => {
    res.status(200);
    res.contentType('text/html');

    const templateVars = {
        pageName: 'Home',
        pageContent: 'Welcome to the home page! This is an Express application with EJS enabled.'
    };

    res.render('home', templateVars);
});

app.get('/about', (req, res) => {
    res.status(200);
    res.contentType('text/html');
    res.render('about');
});

/////////////////////////////////////////////////////////////////////////////////
// 404: Page Not Found
/////////////////////////////////////////////////////////////////////////////////

app.use((req, res, next) => {
    res.status(404);
    res.contentType('text/html');
    res.render('404');
});
